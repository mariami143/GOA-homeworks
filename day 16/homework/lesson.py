for i in range(1, 20):
    print(i)

for i in range(1, 20, 2):
    print(i)

#for ციკლს ვიყენებთ როდესაც ვიცით კოდი რამდენჯერ გაეშვას ხოლო while'ს როდესაც არ ვიცით

#forever loop'ი არის როდესაც while ციკლის პირობა სწორია


counter = 15
while counter < 20:
    print(counter)
    counter = counter + 1


